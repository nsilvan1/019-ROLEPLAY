const a = {
    updateStreet: (data) => {
        $(".second-info").css({"opacity": ".85"});
        let street = data.street;
        $("#street").text(street);
    },
    updateTime: (data) => {
        let time = data.time;
        $("#time").text(time);
    },
    updateRadio: (data) => {
        let frequency = data.frequency;
        let radio = $("#radio");
        if (frequency > 0) {
            $(".radio").css("opacity", "1");
            radio.text(frequency + " hz");
        }
        else {
            $(".radio").css("opacity", "0");
            radio.text(frequency + " hz");
        }
    },
}

